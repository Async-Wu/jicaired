public class  GetNumModel {

    private  boolean dan;
    private int count;
    private  int maxSP;
    private int mixSP;

    public int getMaxSP() {
        return maxSP;
    }

    public void setMaxSP(int maxSP) {
        this.maxSP = maxSP;
    }

    public int getMixSP() {
        return mixSP;
    }

    public void setMixSP(int mixSP) {
        this.mixSP = mixSP;
    }

    public boolean isDan() {
        return dan;
    }

    public void setDan(boolean dan) {
        this.dan = dan;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
